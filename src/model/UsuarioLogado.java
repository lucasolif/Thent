
package model;


public class UsuarioLogado {
    
    Integer codUsuario;
    String nomeUsuario;
    Igreja igrejaUsuario;

    public Integer getCodUsuario() {
        return codUsuario;
    }

    public void setCodUsuario(Integer codUsuario) {
        this.codUsuario = codUsuario;
    }

    public String getNomeUsuario() {
        return nomeUsuario;
    }

    public void setNomeUsuario(String nomeUsuario) {
        this.nomeUsuario = nomeUsuario;
    }

    public Igreja getIgrejaUsuario() {
        return igrejaUsuario;
    }

    public void setIgrejaUsuario(Igreja igrejaUsuario) {
        this.igrejaUsuario = igrejaUsuario;
    }
    
    
    
}
